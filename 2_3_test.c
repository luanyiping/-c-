// 字符串左旋

// 实现一个函数，可以左旋字符串中的k个字符。

// ABCD左旋一个字符得到BCDA

// ABCD左旋两个字符得到CDAB

//  一.low的算法(暴力求解)
#include<stdio.h>
#include<string.h>
#include<assert.h>
void  rotate_string(char* arr, int k)
{
	assert(arr);
	int i = 0;
	int len = strlen(arr);
	// 左旋k个字符
	for (i = 0; i < k; i++)
	{
		char tmp = *arr;
		int j = 0;
		for (j = 0; j < len - 1; j++)
		{
			*(arr + j) = *(arr + j + 1);
		}
		*(arr + len - 1) = tmp;
	}
}

 // 二.三步翻转法
 // ab cdef 
 // ba fedc 分别逆序
 // cdfeba  逆序整个串
void reverse_string(char* left, char* right)
{
	assert(left && right);
	while (left < right)
	{
		char tmp = *left;
		*left = *right;
		*right = tmp;
		left++;
		right--;
	}
}
void rotate_string(char* arr, int k)
{
	assert(arr);
	int len = strlen(arr);
	assert(k <= len);
	reverse_string(arr, arr + k - 1);
	reverse_string(arr + k,arr + len - 1);
	reverse_string(arr, arr + len - 1);
}
int main()
{
	char arr[] = "abcdef";
	int k = 0;
	scanf("%d", &k);
	rotate_string(arr, k);
	printf("%s\n", arr);
}


